package lunarlander;

public class SomePrimitives {

  private String name = "Mateusz";
  private int age = 20;
  private int id = 2137;

  public SomePrimitives(String name, int age, int id) {
    this.name = name;
    this.age = age;
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public int getAge() {
    return age;
  }

  public int getId() {
    return id;
  }
}
